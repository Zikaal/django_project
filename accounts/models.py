from django.conf import settings
from django.db import models

from companies.models import OilCompany


class Profile(models.Model):
    """
    Расширенный профиль пользователя.

    Зачем нужен:
    - стандартная модель пользователя Django хранит только базовые данные
      вроде username, email, first_name, last_name;
    - проекту нужны дополнительные поля, связанные с рабочим контекстом:
      компания, отдел, телефон, описание и аватар;
    - поэтому используется отдельная модель Profile, связанная с User
      через OneToOneField.

    Идея:
    - один пользователь <-> один профиль;
    - профиль удаляется автоматически при удалении пользователя.
    """

    user = models.OneToOneField(
        settings.AUTH_USER_MODEL,
        on_delete=models.CASCADE,
        related_name="profile",
        verbose_name="Пользователь",
    )
    # OneToOneField означает:
    # - у каждого пользователя может быть только один профиль;
    # - к профилю можно обращаться как user.profile благодаря related_name="profile".

    oil_company = models.ForeignKey(
        OilCompany,
        on_delete=models.CASCADE,
        related_name="employees",
        verbose_name="Нефтяная компания",
        null=True,
        blank=True,
        error_messages={
            "blank": "Выберите нефтяную компанию.",
            "invalid": "Выберите корректную нефтяную компанию.",
        },
    )
    # Компания, к которой привязан пользователь.
    #
    # Почему null=True и blank=True:
    # - не каждый пользователь обязан быть привязан к компании сразу;
    # - например, администратор может быть глобальным пользователем системы.
    #
    # related_name="employees" позволяет писать:
    #   company.employees.all()
    # чтобы получить все профили сотрудников этой компании.
    #
    # Важно:
    # on_delete=models.CASCADE означает, что если удалить компанию,
    # все связанные профили тоже будут удалены.
    # Для бизнес-систем иногда безопаснее использовать SET_NULL,
    # чтобы не потерять пользователей при удалении компании.

    bio = models.TextField(
        "О себе",
        blank=True,
        error_messages={
            "invalid": "Введите корректную информацию о себе.",
        },
    )
    # Свободное текстовое поле с краткой информацией о пользователе.
    # blank=True делает поле необязательным на уровне форм и валидации.

    department = models.CharField(
        "Отдел",
        max_length=150,
        error_messages={
            "blank": "Укажите отдел.",
            "invalid": "Введите корректное название отдела.",
        },
    )
    # Название отдела пользователя.
    #
    # Сейчас поле обязательное, потому что blank=True не указан.
    # Но в signals.py профиль создается с department="",
    # поэтому пустое значение технически возможно.
    # Для согласованности бизнес-логики стоит решить:
    # либо поле действительно обязательное,
    # либо добавить blank=True.

    phone_number = models.CharField(
        "Телефон",
        max_length=30,
        error_messages={
            "blank": "Укажите номер телефона.",
            "invalid": "Введите корректный номер телефона.",
        },
    )
    # Контактный номер пользователя.
    #
    # Аналогично department:
    # в модели поле обязательное, но в signals.py при автосоздании профиля
    # передается пустая строка.
    # Это работает, но логически лучше явно определить,
    # должно ли поле быть обязательным всегда.

    avatar = models.ImageField(
        "Аватар",
        upload_to="avatars/",
        blank=True,
        null=True,
    )
    # Файл аватара пользователя.
    #
    # upload_to="avatars/" означает, что файлы будут сохраняться в подпапку avatars/.
    # Например: media/avatars/photo.jpg
    #
    # blank=True и null=True позволяют пользователю не загружать аватар вообще.

    def __str__(self):
        """
        Человекочитаемое строковое представление объекта.

        Используется:
        - в Django admin,
        - в shell,
        - в логах,
        - в отладке.

        Возвращаем строку с пользователем, чтобы сразу было понятно,
        чей именно это профиль.
        """
        return f"Профиль пользователя {self.user}"

    class Meta:
        """
        Метаданные модели Profile.

        Здесь задаются:
        - отображаемые названия в Django Admin;
        - сортировка по умолчанию.
        """

        verbose_name = "Профиль пользователя"
        verbose_name_plural = "Профили пользователей"

        # По умолчанию профили будут сортироваться по username пользователя.
        ordering = ["user__username"]
