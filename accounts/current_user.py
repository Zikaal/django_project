from contextvars import ContextVar

# ContextVar — это безопасное хранилище данных на время текущего контекста выполнения.
# В веб-приложении это особенно полезно, чтобы хранить "текущего пользователя"
# отдельно для каждого запроса, не смешивая данные между разными запросами.
_current_user = ContextVar("current_user", default=None)


def set_current_user(user):
    """
    Сохраняет текущего пользователя в ContextVar.

    Обычно вызывается из middleware в начале обработки запроса.

    Что возвращает:
    - token, который нужен для последующего корректного reset().

    Почему это важно:
    - ContextVar работает не как глобальная переменная;
    - чтобы потом безопасно откатить значение, нужен именно этот token.
    """
    return _current_user.set(user)


def get_current_user():
    """
    Возвращает текущего пользователя из ContextVar.

    Где это может быть полезно:
    - в signals;
    - в audit/logging логике;
    - в model/service-слое, где нет прямого доступа к request;
    - в автоматическом заполнении полей created_by / updated_by.

    Если пользователь не был установлен, вернется значение по умолчанию: None.
    """
    return _current_user.get()


def reset_current_user(token):
    """
    Сбрасывает значение ContextVar к предыдущему состоянию.

    Обычно вызывается в middleware в блоке finally
    после завершения обработки запроса.

    Почему нельзя просто ставить None:
    - reset(token) корректно восстанавливает предыдущее состояние контекста;
    - это безопаснее и правильнее для механизма ContextVar.
    """
    _current_user.reset(token)