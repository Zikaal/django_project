from django.db import models


class OilCompany(models.Model):
    """
    Модель нефтяной компании.

    Для чего нужна:
    - хранит базовую информацию о компаниях;
    - используется как справочник для других сущностей проекта;
    - связана со скважинами и профилями пользователей.

    Примеры связанных сущностей:
    - Well -> принадлежит компании;
    - Profile -> сотрудник может быть привязан к компании.
    """

    name = models.CharField(
        "Название компании",
        max_length=255,
        unique=True,
        help_text="Уникальное название нефтяной компании",
    )
    # Уникальное название компании.
    # unique=True защищает от создания дубликатов.

    region = models.CharField(
        "Регион",
        max_length=255,
        help_text="Регион деятельности компании",
    )
    # Регион, в котором работает компания.

    def __str__(self):
        """
        Человекочитаемое строковое представление компании.

        Используется:
        - в Django Admin;
        - в выпадающих списках форм;
        - в shell и логах.
        """
        return self.name

    class Meta:
        """
        Метаданные модели.
        """

        verbose_name = "Нефтяная компания"
        verbose_name_plural = "Нефтяные компании"

        # Сортировка по умолчанию во всех QuerySet'ах модели.
        ordering = ["name"]